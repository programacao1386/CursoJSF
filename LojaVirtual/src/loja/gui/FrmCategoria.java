    private void consultaLista(){
        try{
            Connection con = Conexao.conectar();
            String sql = "select cod_categoria, nome "
                    +"from loja_categoria "
                    +"order by nome";
            String [] colunas = {"Código", "Categoria"};
            tabela.removeAll();
            Object [][] registros = {,};
            tabela.setModel(new DefaultTableModel(registros,colunas){});
            DefaultTableModel dtm = (DefaultTableModel)tabela.getModel();
            Statement stm = con.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            
            //laço que percorre o rs (conjunto de dados)
            while(rs.next()){
                dtm.addRow(
                new String[]{
                    rs.getString("cod_categoria"),
                    rs.getString("nome")
                }
                );
            }
            
            
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null,
                    "Erro ao carregar os dados da tabela.\n"
                    +"Erro: "+e.getMessage());
        }
    }

    private void btInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirActionPerformed
        // TODO add your handling code here:
        String strCat = txtCategoria.getText();
        Categoria cat = new Categoria();
        cat.setNome(strCat);
        
        CategoriaDao dao = new CategoriaDao(cat);
        
        if(dao.insert()){
            JOptionPane.showMessageDialog(null,
                    "1 Nova categoria adicionada com sucesso");
            txtCategoria.setText("");
            txtCategoria.requestFocus();
            consultaLista();
                    
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao cadastrar a categoria"); 
            txtCategoria.setText("");
            txtCategoria.requestFocus();
        }
    }//GEN-LAST:event_btInserirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        int cod = Integer.valueOf(txtCodigo.getText());
        Categoria cat = new Categoria();
        cat.setCod_categoria(cod);
        
        CategoriaDao dao = new CategoriaDao(cat);
        
        if(dao.findAll()){
            txtCategoria.setText(cat.getNome());
        }else{
            JOptionPane.showMessageDialog(null,
                    "A busca não retornou resultados.",
                    "PESQUISA",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
      
        Categoria cat = new Categoria();
        int cod = Integer.valueOf(txtCodigo.getText());
        String novoValor = txtCategoria.getText();
        
        cat.setCod_categoria(cod);
        cat.setNome(novoValor);
        
        CategoriaDao dao = new CategoriaDao(cat);
        if(dao.update()){
            JOptionPane.showMessageDialog(null,
                    "Registro atualizado com sucesso",
                    "Atualização de registro", 
                    JOptionPane.INFORMATION_MESSAGE);
            consultaLista();
        }else{
            JOptionPane.showMessageDialog(null,
                    "Erro ao realizar a atualização.",
                    "Atualização de registro", 
                    JOptionPane.INFORMATION_MESSAGE);
          
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int cod = Integer.valueOf(txtCodigo.getText());
        Categoria cat = new Categoria();
        cat.setCod_categoria(cod);
        CategoriaDao dao = new CategoriaDao(cat);
        
        if(dao.delete()){
            JOptionPane.showMessageDialog(null,"Um registro excluido.");
            consultaLista();
        }else{
            JOptionPane.showMessageDialog(null,"Erro ao excluir a categoria "+cod+"!"); 
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

